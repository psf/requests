    def send(self, request, **kwargs):
        """Send a given PreparedRequest.

        :rtype: requests.Response
        """
        # Set defaults that the hooks can utilize to ensure they always have
        # the correct parameters to reproduce the previous request.
        kwargs.setdefault("stream", self.stream)
        kwargs.setdefault("verify", self.verify)
        kwargs.setdefault("cert", self.cert)
        if "proxies" not in kwargs:
            kwargs["proxies"] = resolve_proxies(request, self.proxies, self.trust_env)

        # It's possible that users might accidentally send a Request object.
        # Guard against that specific failure case.
        if isinstance(request, Request):
            raise ValueError("You can only send PreparedRequests.")

        # Set up variables needed for resolve_redirects and dispatching of hooks
        allow_redirects = kwargs.pop("allow_redirects", True)
        stream = kwargs.get("stream")
        hooks = request.hooks

        # Get the appropriate adapter to use
        adapter = self.get_adapter(url=request.url)

        # Start time (approximately) of the request
        start = preferred_clock()

        # 执行 BEFORE_SEND 阶段的管线
        context = self.pipeline.dispatch(HookStage.BEFORE_SEND, request=request, session=self, adapter=adapter, kwargs=kwargs)
        if context.short_circuit_response:
            return context.short_circuit_response
        request = context.request
        kwargs = context.kwargs

        # Send the request
        r = adapter.send(request, **kwargs)

        # 执行 AFTER_SEND 阶段的管线
        context = self.pipeline.dispatch(HookStage.AFTER_SEND, request=request, response=r, session=self, adapter=adapter)
        if context.short_circuit_response:
            return context.short_circuit_response
        r = context.response

        # Total elapsed time of the request (approximately)
        elapsed = preferred_clock() - start
        r.elapsed = timedelta(seconds=elapsed)

        # Response manipulation hooks
        r = dispatch_hook("response", hooks, r, **kwargs)

        # 执行 AFTER_RESPONSE_HOOKS 阶段的管线
        context = self.pipeline.dispatch(HookStage.AFTER_RESPONSE_HOOKS, request=request, response=r, session=self)
        if context.short_circuit_response:
            return context.short_circuit_response
        r = context.response

        # Persist cookies
        if r.history:
            # If the hooks create history then we want those cookies too
            for resp in r.history:
                extract_cookies_to_jar(self.cookies, resp.request, resp.raw)

        extract_cookies_to_jar(self.cookies, request, r.raw)

        # Redirect resolving generator.
        gen = self.resolve_redirects(r, request, **kwargs)

        # 执行 BEFORE_REDIRECT 阶段的管线
        context = self.pipeline.dispatch(HookStage.BEFORE_REDIRECT, request=request, response=r, session=self, gen=gen)
        if context.short_circuit_response:
            return context.short_circuit_response
        r = context.response
        gen = context.gen

        # Resolve redirects if allowed.
        history = [resp for resp in gen]

        # Shuffle things around if there's history.
        if history:
            # Insert the first (original) request at the start
            history.insert(0, r)
            # Get the last request made
            r = history.pop()
            r.history = tuple(history)

        # If redirects aren't being followed, store the response on the Request for Response.next().
        if not allow_redirects:
            try:
                r._next = next(self.resolve_redirects(r, request, **kwargs))
            except StopIteration:
                pass

        return r

